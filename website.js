// Function to scrape emails from a given URL
async function scrapeEmails(url) {
    const browser = await playwright.chromium.launch();
    const page = await browser.newPage();

    try {
        // Keep the timeout as 3 seconds
        await page.goto(url, { timeout: delayTime });

        // Wait for the page to load
        await page.waitForLoadState('networkidle');

        // Get the page content
        const content = await page.content();

        // Regular expression to find emails
        const emailRegex = /\b[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Z|a-z]{2,}\b/g;
        let emailMatches = content.match(emailRegex);

        // Filter out unwanted emails
        if (emailMatches) {
            emailMatches = emailMatches.filter(email => 
                !emailFilter.some(filterItem => email.includes(filterItem))
            );
        }

        return emailMatches ? Array.from(new Set(emailMatches)) : [];
    } catch (error) {
        if (error.name === 'TimeoutError') {
            console.warn(`Timeout when accessing: ${url}`);
            // Return an empty array or handle the error as needed
            return [];
        } else {
            // Handle other types of errors
            throw error;
        }
    } finally {
        await browser.close();
    }
}

function nameAusEmail(email) {
  // 1. Regex isoliert alles vor dem @-Zeichen
  const match = email.match(/^([^@]+)/);
  if (!match) return null;
  const lokalTeil = match[1].toLowerCase();
  // 2. Umfassende Liste von System-, Funktions- und Abteilungs-E-Mails
  const systemMails = [
    // Allgemein & Kontakt
    'info', 'kontakt', 'contact', 'hello', 'hallo', 'mail', 'email', 'feedback', 'anfrage', 'inquiry',
    // Support & Service
    'support', 'service', 'help', 'hilfe', 'kundenservice', 'customercare', 'team', 'crew', 'office',
    // Vertrieb & Marketing
    'sales', 'vertrieb', 'marketing', 'media', 'press', 'presse', 'pr', 'news', 'newsletter', 'orders', 'bestellung',
    // Administration & IT-Systeme
    'admin', 'administrator', 'root', 'sysadmin', 'webmaster', 'hostmaster', 'postmaster', 'noc', 'dns', 'ftp',
    // Sicherheit & Recht
    'security', 'privacy', 'datenschutz', 'abuse', 'spam', 'compliance', 'phishing', 'legal',
    // Personal & Karriere
    'jobs', 'karriere', 'careers', 'hr', 'personal', 'recruiting', 'bewerbung',
    // Automatische Benachrichtigungen
    'noreply', 'no-reply', 'notification', 'alert', 'status', 'devnull', 'null', 'bot', 'bounce', 'maildaemon'
  ];
  // Falls der extrahierte Teil exakt einem System-Präfix entspricht, wird null zurückgegeben
  if (systemMails.includes(lokalTeil)) {
    return null; 
  }
  // 3. Bereinigung für echte Namen (Punkte, Unterstriche, Bindestriche zu Leerzeichen)
  let name = match[1].replace(/[\._-]/g, ' ');
  // 4. Anfangsbuchstaben groß schreiben (z.B. max.mustermann -> Max Mustermann)
  return name.replace(/\b\w/g, char => char.toUpperCase());
}

// Function to process the CSV file and scrape each website
async function processCsv(cgidIndex) {
    const rows = [];
    const headersSet = new Set();
    const batchSize = 5; // Number of websites to process at once
    const baseURL = `./data/${GCID[cgidIndex][0]}_${cgidIndex}/${GCID[cgidIndex][0]}_${cgidIndex}_clean.csv`;

    const readStream = fs.createReadStream(baseURL)
        .pipe(csv());

    const allRows = [];

    for await (const row of readStream) {
        allRows.push(row);
    }

    for (let i = 0; i < allRows.length; i += batchSize) {
        const batch = allRows.slice(i, i + batchSize);
        const batchPromises = batch.map(async row => {
            const newRow = { ...row };
            const website = row.Website || '';

            if (website) {
                console.log(`Scraping: ${website}`);
                console.log(`Done (${GCID[cgidIndex][0]}_${cgidIndex}): ${Math.round((i+1)/allRows.length * 10000)/100}%.`);
                const emails = await scrapeEmails(website);
                emails.forEach((email, index) => {
                    const emailHeader = `Email${index + 1}`;
                    const emailName = nameAusEmail(email);
                    newRow[emailHeader] = email + (emailName ? ` (${emailName})` : '');
                    headersSet.add(emailHeader);
                });
            }

            return newRow;
        });

        const batchResults = await Promise.allSettled(batchPromises);
        batchResults.forEach(result => {
            if (result.status === 'fulfilled') {
                rows.push(result.value);
            }
        });
    }

    // Combine original headers with new email headers
    const headers = Object.keys(rows[0]).concat(Array.from(headersSet));

    return { rows, headers, baseURL };
}


// Function to write the data to a new CSV file
async function writeCsv({ rows, headers, baseURL }) {
    const outputFileName = baseURL.replace('.csv', '_emails.csv');

    const csvWriter = createCsvWriter({
        path: outputFileName,
        header: headers.map(header => ({ id: header, title: header }))
    });

    await csvWriter.writeRecords(rows);
    console.log(`CSV file written successfully to ${outputFileName}`);
}

// Start the process
const CGID_INDEX_START = 1;
const CGID_INDEX_END = 1;

for (let cgidCategoryIndex = CGID_INDEX_START; cgidCategoryIndex <= CGID_INDEX_END; cgidCategoryIndex++) {
    processCsv(cgidCategoryIndex).then(writeCsv);
}
